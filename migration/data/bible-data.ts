export function getBookByName(name: string): BibleBook | undefined {
  return BIBLE_BOOKS.find((book) => book.name === name)
}

export function getBookId(bookName: string): string | undefined {
  return BOOK_ID_MAP[bookName]
}

export function getOldTestamentBooks(): BibleBook[] {
  return BIBLE_BOOKS.filter((book) => book.testament === "Old")
}

export function getNewTestamentBooks(): BibleBook[] {
  return BIBLE_BOOKS.filter((book) => book.testament === "New")
}

export function getLocalizedBookName(bookName: string, language: Language): string {
  if (language === "cuv" || language === "cu1") {
    return BOOK_NAMES_ZH[bookName] || bookName
  }
  return bookName
}

export function getLocalizedTestamentName(testament: "Old" | "New", language: Language): string {
  if (language === "cuv" || language === "cu1") {
    return testament === "Old" ? "舊約" : "新約"
  }
  return testament === "Old" ? "Old Testament" : "New Testament"
}
